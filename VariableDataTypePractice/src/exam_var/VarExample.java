package exam_var;

public class VarExample {
    static void main(String[] args){
        var price= 10000;
        var name= "홍길동";
        var pi= 3.141592;

        System.out.println("price= " + price);
        System.out.println("name= " + name);
        System.out.println("pi= " + pi);

    }
}
