package exam_operation;

public class AssigmentOperatorExample {
    static void main(String[] args){
        int result= 0;
        result +=10; //10
        System.out.println(result);

        result -=5; //5
        System.out.println(result);

        result *=3; //15
        System.out.println(result);

        result /=5; //3
        System.out.println(result);

        result %=3; //0
        System.out.println(result);
    }
}
